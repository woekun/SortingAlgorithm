package BubbleSort;

/**
 *
 * @author HYPPY
 */
public class BubbleSort {

    boolean swapped = true;
    int j = 0;

    public void bubbleSort(int arr[]) {
        while (swapped) {
            swapped = false;
            j++;
            for (int i = 0; i < arr.length - j; i++) {
                if (arr[i] > arr[i + 1]) {
                    int tmp = arr[i];
                    arr[i] = arr[i + 1];
                    arr[i + 1] = tmp;
                    swapped = true;
                }
            }
        }
    }
}
